bool uart_update()
{
	u8 i;
	if (USART_RX_STA & 0x8000)
	{
		if (USART_RX_STA & 0x3fff == sizeof(inf))
		{
			for (i=0;i<sizeof(inf);++i)
			{
				((u8*)&info)[i]=USART_RX_BUF[i];
			}
			id=info.id;
			x=info.px[id];
			y=info.py[id];
			USART_RX_STA=0;
			return true;
		}else{
			USART_RX_STA=0;
			return false;
		}
		
	}else{
		return false;
	}
}

void uart_send(u8 target,u8 wid)
{
	u8 data=0;
	data|=(info.id&0x03)<<6;
	data|=(target&0x03)<<4;
	data|=(wid&0x0f);
	USART_SendData(USART1,data);
	while(USART_GetFlagStatus(USART1,USART_FLAG_TC)!=SET);
	USART_SendData(USART1,0x0d);
	while(USART_GetFlagStatus(USART1,USART_FLAG_TC)!=SET);
	USART_SendData(USART1,0x0a);
	while(USART_GetFlagStatus(USART1,USART_FLAG_TC)!=SET);
}
